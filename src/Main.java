import java.util.Random;

public class Main {
    public static void main(String[] args) {
        // 1. Оголошення змінних
        short s1 = -5;           // змінна типу short (2 байти)
        double d1 = -31.9;        // змінна типу double (8 байтів)
        boolean b;                // змінна типу boolean
        long l;                   // змінна типу long (8 байтів)

        // 2. Виконання дій зі змінними

        // Привласнюємо значення змінної d1 змінній l (явне приведення)
        l = (long) d1;  // Приведення типів: перетворення double в long
        System.out.println("Значення змінної l після приведення d1: " + l);

        // Привласнення випадкового значення в діапазоні від -100 до 100 змінній d1
        Random rand = new Random();
        d1 = -100 + (200) * rand.nextDouble(); // випадкове значення в діапазоні [-100, 100]
        System.out.println("Нове випадкове значення змінної d1: " + d1);

        // Привласнення значення false змінній b
        b = false;
        System.out.println("Значення змінної b: " + b);

        // Оголошення додаткових змінних
        double d2 = 3.14;   // Задано як приклад значення
        short s2 = 2;       // Задано як приклад значення

        // Обчислення добутку s1 та s2
        int product = s1 * s2;  // Добуток цілих чисел
        System.out.println("Добуток s1 та s2: " + product);

        // 3. Обчислення виразу y
        // Використовуємо математичні функції Math для обчислення тангенса
        double y = ((Math.tan(l) + Math.tan(s1)) / (l - s1)) + ((Math.pow(l, 2) - Math.pow(s1, 2)) / (Math.pow(l, 3) + 2 * l + s1 + 3));

        // Виведення результату обчислень виразу
        System.out.println("Результат обчислення виразу y: " + y);
    }
}
