import java.util.Random;

public class Main {
    public static void main(String[] args) {
        // 1. Оголошення змінних
        short s1 = -5;   // змінна s1, ціле число, розмір 2 байти, значення -5
        int s2;          // змінна s2, ціле число, розмір 4 байти, без початкового значення
        double d1 = -31.9; // змінна d1, дійсне число, розмір 8 байтів, значення -31.9
        float d2;        // змінна d2, дійсне число, розмір 4 байти, без початкового значення
        boolean b;       // змінна b, логічний тип
        long l;          // змінна l, ціле число, розмір 8 байтів

        // 2. Виконання дій зі змінними

        l = (long) d1; // Привласнюємо змінній l значення змінної d1, перетворюючи double на long
        Random rand = new Random();
        d1 = rand.nextDouble() * 200 - 100; // Присвоюємо d1 випадкове значення в діапазоні від -100 до 100
        b = false; // Присвоюємо змінній b значення false
        s2 = 10; // Надаємо значення s2 для коректного обчислення, наприклад, 10
        d2 = s1 * s2; // Привласнюємо d2 значення добутку s1 та s2, тип float може автоматично перетворити результат

        // Виведення результатів на екран
        System.out.println("Значення змінної l (перетворення з d1): " + l);
        System.out.println("Нове значення змінної d1 (випадкове): " + d1);
        System.out.println("Значення змінної b: " + b);
        System.out.println("Значення змінної d2 (добуток s1 і s2): " + d2);

        // 3. Обчислення виразу y
        // Використовуємо математичні функції Math для обчислення тангенса
        double y = ((Math.tan(l) + Math.tan(s1)) / (l - s1)) + ((Math.pow(l, 2) - Math.pow(s1, 2)) / (Math.pow(l, 3) + 2 * l + s1 + 3));

        // Виведення результату обчислень виразу
        System.out.println("Результат обчислення виразу y: " + y);
    }
}
